/**
 * @param {number[][]} isConnected
 * @return {number}
 */
var findCircleNum = function (isConnected) {
  let ans = 0,
    n = isConnected.length,
    m = isConnected[0].length;
  const marked = {};
  const trav = (i, j) => {
    marked[i] = true;
    marked[j] = true;
    isConnected[i][j] = 0;
    isConnected[j][i] = 0;
    for (let k = 0; k < m; k++) {
      if (j !== k && isConnected[j][k] === 1) {
        trav(j, k);
      }
    }
  };
  for (let i = 0; i < n; i++) {
      let hasMarked = false;
    for (let j = 0; j < m; j++) {
      if (i !== j && isConnected[i][j] === 1) {
        trav(i, j);
        if (!hasMarked) {
            ans += 1;
            hasMarked = true;
        }
      }
    }

    if (!marked[i]) {
      ans += 1;
    }
  }

  return ans;
};

